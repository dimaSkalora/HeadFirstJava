package head_04;

public class ElectricGuitar {
    String brand;
    int numOgPickups;
    boolean rockStarUseeIt;

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getNumOgPickups() {
        return numOgPickups;
    }

    public void setNumOgPickups(int numOgPickups) {
        this.numOgPickups = numOgPickups;
    }

    public boolean isRockStarUseeIt() {
        return rockStarUseeIt;
    }

    public void setRockStarUseeIt(boolean rockStarUseeIt) {
        this.rockStarUseeIt = rockStarUseeIt;
    }
}
